#include "libft.h"

int	ft_isalnum(int c)
{
	if (ft_isalpha(c) || ft_isdigit(c))
	{
		return (1);
	}
	return (0);
}
/*This function operaters alphanumeric character test
 A-Z & a-z & 0-9
 1 = true and 0 is false*/
